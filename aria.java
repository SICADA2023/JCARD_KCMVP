    public static void RotXOR(short [] S, short n, short[] t, short rott){
        short i, q;
        q=(short)(n/8);
        n %= 8;
        for(i=0;i<16;i++){
            t[rott+(((q+i)%16))]^=(S[i]>>n);
            if(n!=0){
                t[rott+((q+i+1) % 16)] ^= ((S[i] << (8-n))%256);
            }
        }
    }

    public static short power_2(byte a){
        short b=1;
        for(short i=0;i<a;i++){
            b*=2;
        }
        return b;
    }

    public static short s_to_u(byte a){
        short b=0, c;
        for(short i=0;i<8;i++){
            c=(short)(a>>(7 - i) & 1);
            if(c==1){
                b+=power_2((byte)(7-i));
            }
        }
        return b;
    }
